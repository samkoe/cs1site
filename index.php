<a name="collaboration-policy"></a>
<h2>Collaboration Policy</h2>
  <p>All students should read the Petra Academy Student Handbook.</p>
  <p>When it comes to Python assignments, you MAY</p>
    <ul>
    <li>Work with the other people on your team if teams are allowed. Each assignment will specify the maximum number of people per team (usually only 2).</li>
    <li>Share ideas with people in other teams.</li>
    <li>Help other teams troubleshoot problems.</li>
    </ul>

  <p>You may NOT</p>
    <ul>
    <li>Share code you write with other teams.</li>
    <li>Submit code that someone on your team did not write. This includes code found on the internet.</li>
    <li>Modify another team&#39;s solution and claim it as your own.</li>
    </ul>
  <p>Failure to abide by these rules will result in an &quot;F&quot; on the assignment and a meeting with the headmaster.</p>

