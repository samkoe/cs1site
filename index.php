<h2 id="course-information">Course Information</h2>
<p><strong>Description:</strong>  This course introduces students to computational thinking, which is a way of solving problems using computers. It also explores the nature of computers as tools and how these tools shape us and affect our definition of what it means to be human.</p>
<p><strong>Course Outcomes</strong></p>
<ul>
<li>Understand what computational thinking is and how it helps us solve a variety of problems.</li>
<li>Design and implement short programs in an interpreted language such as Python.</li>
<li>Appreciate the variety of ways in which computing affects the human condition in both positive and negative ways.</li>
<li>Understand the basic philosophical and theological issues involved in the application of technology.</li>
<li>Understand some of the significant ideas and people that underlie computing&#39;s past, present and future.</li>
</ul>

