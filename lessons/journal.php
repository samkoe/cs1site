<h1>Rubric for Journal Entries</h1>
<p>Each journal entry is worth 20 points. A good journal entry contains the following:</p>
<ul>
  <li>5 pts -- The entry is on topic and cogent.</li>
  <li>5 pts -- The entry is written in good style and devoid of grammar, usage, and mechanics errors</li>
  <li>10 pts -- The entry engages the topic intelligently by
    <ol>
      <li>responding to the central issue or idea with insightful questions and commentary</li>
      <li>making an insightful connection to previous discussions about computing topics or to relevant readings from humanities class</li>
    </ol>
  </li>
</ul>

