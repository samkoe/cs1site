<h2>Minecraft Tile - 100 points</h2>
<p>After producing business cards for the previous assignments, you decide to foray into the fascinating world of <a href="http://en.wikipedia.org/wiki/Minecraft">Minecraft</a>. Inspired by the likes of Picasso, Van Gogh, and Monet, you would like to create your own computer artwork and be immortalized forever. As a first step, you have been hired to create a tile that can be used in Minecraft play.</p>

<p>Use Python Turtle Graphics to design and draw a simple Minecraft tile. Here is a <a href="http://www.cs.montana.edu/paxton/classes/joy-and-beauty/assignments/hw3/brick.png">picture</a> of one such tile that could potentially earn the first 80 points below. (It would not earn the 20 creativity points.) This particular tile is supposed to be a dirt brick with grass at its top.</p>

<h3>Requirements and Grading</h3>
<ul>
  <li>20 points - The tile is centered both horizontally (10 points) and vertically(10 points) in the drawing area.</li>
  <li>20 points - The tile is 100 pixels wide (10 points) and 100 pixels tall (10 points).</li>
  <li>20 points - The tile contains at least two different colors (10 points each).</li>
  <li>10 points - An appropriate comment appears at the top of the Python solution.</li>
  <li>10 points - The Python solution is easy to understand and does not contain unnecessary code.</li>
  <li>20 points - Creativity points. Do something creative in addition to the requirements above. For example, enhance the tile so that it would impress your friends, make a second tile that is quite different from the first (it could be placed anywhere and be of any size), add texture to the tile, etc. Feel free to find inspiration by Googling for Minecraft images.</li>
</ul>

<h3>Helpful Hint</h3>
<p>Take a look at the turtle method <a href="http://interactivepython.org/runestone/static/thinkcspy/PythonTurtle/SummaryofTurtleMethods.html">summary</a> to remind yourself what is possible.</p>

<h3>Submission</h3>
<p>Place the solution in a file named <strong>Minecraft.py</strong> and submit it in the CS GDrive no later than midnight on Friday, October 28. Late submissions receive no credit, but partial credit can be earned by making an ontime submission.</p>
